    private void telaTrocarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_telaTrocarUsuarioActionPerformed
        // Bot칚o trocar de usu치rio, efetuar logout
        frmLogin telaLogin = new frmLogin();
        int retorno;

        retorno = JOptionPane.showConfirmDialog(null, "Desejar trocar de usu치rio?");

        //Condi칞칚o para caso usu치rio n칚o desejar trocar o usu치rio.
        if (retorno == 0) {
            this.dispose();
            telaLogin.setVisible(true);
        }


    }//GEN-LAST:event_telaTrocarUsuarioActionPerformed

