    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // Bot√£o consultar
        try {
            
            DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            LocalDate data_venda = LocalDate.parse(txt_DataVenda.getText(), formato);
            
            double total_venda;
            VendasDAO dao = new VendasDAO();
            total_venda = dao.retornarTotalVendaPorData(data_venda);
            
            txtTotalVenda.setText(String.valueOf(total_venda));
            
        } catch (Exception erroSQL) {
            JOptionPane.showMessageDialog(null, "Informe a data correta!" + erroSQL);
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

